// FOR LOOP
//Modified from W3 Schools https://www.w3schools.com/cpp/cpp_for_loop.asp
for (int i = 0; i < 10; i++) {
  cout << i << "\n";
}


//WHILE LOOP
//Modified from https://www.w3schools.com/cpp/cpp_while_loop.asp
int i = 0;
while (i < 10) {
  cout << i << "\n";
  i++;
}



//DO/WHILE LOOP
//Modified from https://www.w3schools.com/cpp/cpp_do_while_loop.asp
int i = 0;
do {
  cout << i << "\n";
  i++;
}
while (i < 10);
